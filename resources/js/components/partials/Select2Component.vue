<template>
        <select style="font-size: 12px;" class="form-control myFont">
            <option value="0" disabled><strong> {{ name }} </strong></option>
            <slot></slot>
        </select>
</template>

<style>
    .myFont {
        font-size: 12px;
    }
</style>
<script>
    export default {
        props: ['options', 'value', 'name'],
        mounted: function () {
            var vm = this
            $(this.$el)
            // init select2
                .select2({ data: this.options,
                    dropdownCssClass: "myFont"})
                .val(this.value)
                .trigger('change')
                // emit event on change.
                .on('change', function () {
                    vm.$emit('input', this.value)
                })
        },
        watch: {
            value: function (value) {
                // update value
                $(this.$el)
                    .val(value)
                    .trigger('change')
            },
            options: function (options) {
                // update options
                $(this.$el).empty().select2({ data: options })
            }
        },
        destroyed: function () {
            $(this.$el).off().select2('destroy')
        }
    }
</script>
